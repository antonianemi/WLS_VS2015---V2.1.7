void GetTime(char *str)												//get time from system
{
time_t tiempo;
struct tm *tmPtr;
tiempo = time(NULL);
tmPtr = localtime(&tiempo);
strftime(str,32,(char*)"%Y-%m-%d %H:%M:%S",tmPtr);
}

